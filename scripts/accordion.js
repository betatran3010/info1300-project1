$("#subcontainer-phonetics").on("click", function () {
    if ($("#phonetics-content").hasClass("accessible-hidden")) {
        $("#phonetics-content").removeClass("accessible-hidden");
        $("#right-pointing-1").addClass("hidden");
        $("#down-pointing-1").removeClass("hidden");
    } else {
        $("#phonetics-content").addClass("accessible-hidden");
        $("#right-pointing-1").removeClass("hidden");
        $("#down-pointing-1").addClass("hidden");
    }
});

